package model;

public class Fans {
	private int num;
	private String ID;
	private String C_ID;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getID() {
		return ID;
	}
	public void setID(String ID) {
		this.ID = ID;
	}
	public String getC_ID() {
		return C_ID;
	}
	public void setC_ID(String C_ID) {
		this.C_ID = C_ID;
	}

}
